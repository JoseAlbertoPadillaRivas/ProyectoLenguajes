    private void cargarRegistros() {
        try {
            // Declaramos una lista que almacena los objetos cUsuario
            // La lista se inicializa llamando al método obtenerUsuarios del objeto usuario
            List<cUsuario> listaUsuarios = usuario.ObtenerUsuarios();

            // Crear un DefaultTableModel con los nombres de las columnas
            DefaultTableModel modeloTabla = new DefaultTableModel();
            modeloTabla.addColumn("Cedula");
            modeloTabla.addColumn("Nombre");
            modeloTabla.addColumn("Apellido");
            modeloTabla.addColumn("Username");
            modeloTabla.addColumn("Contraseña");
            modeloTabla.addColumn("IDRol");

            for (cUsuario usuario : listaUsuarios) {
                Object[] fila = {
                    usuario.getCedula(),
                    usuario.getNombre(),
                    usuario.getApellidos(),
                    usuario.getUsername(),
                    usuario.getPassword(),
                    usuario.getIdrol(),};
                modeloTabla.addRow(fila);
            }

            tblUsuarios.setModel(modeloTabla);
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los registros de usuarios.");
        }
    }

