    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int idCategoria = Integer.parseInt(txtCategoria.getText());
        String nombre = txtNombre.getText();

        cCategoria editarCategoria = new cCategoria(idCategoria, nombre);
        try {
            cCrudCategoria crud = new cCrudCategoria();
            crud.editarCategoria(editarCategoria);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
        cargarRegistros();
        limpiar();

    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed

        int idCategoria = Integer.parseInt(txtCategoria.getText());
        cCategoria eliminarCategoria = new cCategoria(idCategoria);
        try {
            cCrudCategoria crud = new cCrudCategoria();
            crud.eliminarCategoria(eliminarCategoria);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
        cargarRegistros();
        limpiar();    
        
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnCargarCamposActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCargarCamposActionPerformed
        try {
            int modificar = tbCategoria.getSelectedRow();
            if (modificar >= 0) {
                txtCategoria.setText(tbCategoria.getValueAt(modificar, 0).toString());
                txtNombre.setText(tbCategoria.getValueAt(modificar,1).toString());
            } else {
                JOptionPane.showMessageDialog(null, "No ha selecionado una fila para modificar");
            }
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnCargarCamposActionPerformed

