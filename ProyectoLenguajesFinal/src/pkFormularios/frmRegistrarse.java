    private void btnRegistrarseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarseActionPerformed

        int cedula = Integer.parseInt(txtIdUsuario.getText());
        String nombre = txtNombre.getText();
        String apellidos = txtApellido.getText();
        String username = txtUsername.getText();
        String password = txtContrasenna.getText();
        int idRol = Integer.parseInt(txtidRol.getText());
        
        
        cUsuario nuevoUsuario = new cUsuario(cedula, nombre, apellidos, username, password, idRol);
        
        try{
            cCrudUsuario crud = new cCrudUsuario();
            crud.insertarUsuario(nuevoUsuario);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Error: " + e);
        }
        frmLogin Login = new frmLogin();
        Login.setVisible(true);
        Login.setLocationRelativeTo(null);
        dispose();
    }//GEN-LAST:event_btnRegistrarseActionPerformed

